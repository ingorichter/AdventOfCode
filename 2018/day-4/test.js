import test from 'ava';
const reposeRecord = require('./ReposeRecord');
const reposeRecord2 = require('./ReposeRecord-2');

test('should calculate which guard sleeps most during the midnight shift', t => {
    const guardSleepLog =
	  `[1518-11-01 00:00] Guard #10 begins shift
[1518-11-01 00:05] falls asleep
[1518-11-01 00:25] wakes up
[1518-11-01 00:30] falls asleep
[1518-11-01 00:55] wakes up
[1518-11-01 23:58] Guard #99 begins shift
[1518-11-02 00:40] falls asleep
[1518-11-02 00:50] wakes up
[1518-11-03 00:05] Guard #10 begins shift
[1518-11-03 00:24] falls asleep
[1518-11-03 00:29] wakes up
[1518-11-04 00:02] Guard #99 begins shift
[1518-11-04 00:36] falls asleep
[1518-11-04 00:46] wakes up
[1518-11-05 00:03] Guard #99 begins shift
[1518-11-05 00:45] falls asleep
[1518-11-05 00:55] wakes up`;

    t.plan(1);

    // guard 10 sleeps the most on minute 24
    t.is(10 * 24, reposeRecord(guardSleepLog));
});

